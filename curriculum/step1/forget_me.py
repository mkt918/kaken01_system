# forget_me.py - 「記憶力ゼロ」のモンスター図鑑
# 中学生が10分で書けて、SQL（データ保存）の必要性がわかるコードなのだ！

monsters = [] # 捕まえたモンスターを入れる「箱（リスト）」

print("--- ⚔️ モンスター図鑑 ⚔️ ---")
print("捕まえたモンスターの名前を入れてね！ ( 'q' でおしまい)")

while True:
    name = input("モンスター名 > ")
    
    if name == 'q':
        break
    
    monsters.append(name)
    print(f"現在の図鑑: {monsters}")

print("\nプログラムを終了したのだ。")
print("...でも、もう一回動かしてみると、せっかく捕まえたモンスターが消えちゃうのだ！")
print("昨日捕まえたモンスターを『日記（データベース）』に書き留める方法を学ぼう！")
