# monster_v2.py - ⚔️ じっくり作る！最強のモンスター図鑑 ⚔️

# --- 【ステップ1：準備】 ---
# まずは「モンスターを入れるためのリスト（箱）」を用意するよ。
monsters = [] 

print("==============================")
print("   🏰 モンスター管理システム 🏰   ")
print("==============================")

# --- 【ステップ2：入力を受け取る】 ---
# while True: を使うと、ずっと入力を続けることができるよ。
while True:
    print("\n[今の図鑑]:", monsters)
    name = input("モンスターの名前を入れてね (qで終了): ")
    
    # もし 'q' と入力されたら、ループを抜ける（おしまい）
    if name == 'q':
        print(">>> システムを終了するのだ！")
        break
    
    # リストの一番後ろに名前を追加する
    monsters.append(name)
    
    # --- 【ステップ3：おまけ機能（レベルアップ！）】 ---
    # input() で受け取った数字は「文字」なので int() に変える必要があるよ。
    level_str = input(f"{name}のレベルを教えて？: ")
    level = int(level_str) 
    
    print(f"✨ 登録完了！ {name} (Lv.{level}) ✨")

# --- 【ステップ4：さいごに】 ---
print("\n--- 最終結果 ---")
for m in monsters:
    print(f"・{m}")

print("\n...でも気をつけて！プログラムを閉じると、この子たちは消えちゃうのだ。")
print("次回は「データベース」を使って、何があっても消えない図鑑を作るよ！")
