# week01_deep_dive.py - 📦 プログラミングの第一歩：変数と型 📦

# --- 【イントロ】変数ってなんだろう？ ---
# 変数は、コンピュータの中にある「データを入れる名前付きの箱」のこと。
# 中学生にもわかりやすく、180分かけてじっくり付き合います。

print("--- 1. 変数を作ってみよう ---")
name = "たろう" # 文字の型 (str)
age = 14        # 整数（数字）の型 (int)

print(f"名前は {name}、年齢は {age} 歳だね。")

# --- 【チャレンジ1】型を確認してみよう ---
# プログラムの中で、その箱が「何の種類」か調べる魔法の言葉 type()
print("\n--- 2. 型を調べてみよう ---")
print("nameの型:", type(name))
print("ageの型:", type(age))

# --- 【重要】種類が違うと、足し算ができない！？ ---
# 文字の "10" と、数字の 10 は別物。
print("\n--- 3. 失敗から学ぼう ---")
num_text = "10"
num_real = 10

# [わざとエラーを出してみる区間]
# 下の行のコメントを外して動かすと、エラーが出るよ！
# print(num_text + num_real) 
# -> TypeError: can only concatenate str (not "int") to str

print("ヒント：計算するには int(num_text) にしなきゃいけないんだ！")

