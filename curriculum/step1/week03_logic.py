# week03_logic.py - 🚦 プログラムに「判断」をさせよう：条件分岐 🚦

# --- 【イントロ】「もしも〜なら」を考えよう ---
# プログラムが賢くなる最初のステップ。
# Python独特のルール「インデント（字下げ）」を180分かけて体に染み込ませます。

print("--- 1. True or False？（正しいか、間違いか） ---")
# パソコンは「Yes」か「No」でしか判断できない！？
my_score = 85
print(f"君の点数: {my_score}")

print("点数は80点より高い？:", my_score > 80) # True（正しい）が出るね
print("点数は100点と同じ？:", my_score == 100) # False（間違い）が出るね

# --- 【重要】魔法のルール：インデント ---
# if の次の行は「スペース4つ」空ける。これがPythonのルール！
print("\n--- 2. if文を動かしてみよう ---")
age = int(input("君の年齢は？: "))

if age >= 20:
    print("大人の仲間入りだね！")
    print("ここも if が正しい時だけ動くよ。")
else:
    print("まだ子供だね。ゆっくり成長しよう！")

print("ここは if に関係なく、いつでも動くよ（インデントがないから）。")

